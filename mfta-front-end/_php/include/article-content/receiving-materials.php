    <h2>Recipient Eligibility</h2>

    <p>All MFTA recipient organizations must operate within the five boroughs of New York City. They must also fall into one of the following categories:</p>

    <ul>
        <li>
            <strong class="d-block">Nonprofit arts/cultural groups</strong> 
            Must have at least two years of established ongoing arts programming.
        </li>
        <li><strong class="d-block">Fiscally Sponsored individuals, Organizations and Projects</strong> 
            Must have at least least two years of established ongoing arts programming.</li>
        <li>
            <strong class="d-block">Nonprofit community service, social service, health, and environmental organizations</strong> 
            Must have at least two years of established ongoing arts programming.
        </li>
        <li>
            <strong class="d-block">NYC Public Schools</strong>
            93% of DoE schools have MFTA accounts <a href="#">check if your school already has one</a>.
        </li>
        <li>
            <strong class="d-block">New York City, State, or Federal Agencies</strong> 
            Including Public Libraries, CUNY, and SUNY.
        </li>
    </ul>

